    void LightSource::render() const {
        m_pShader->use();
        m_pShader->setVec3("lightColor", m_color[0], m_color[1], m_color[2]);
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, m_pos);
        model = glm::scale(model, glm::vec3(0.2f));
        m_pShader->setMatrix("model", glm::value_ptr(model));
        glBindVertexArray(m_VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
    }

    std::shared_ptr<ShaderProgram>& LightSource::shader() {
        return m_pShader;
    }

    void Cube::render() const {
        m_pShader->use();
        m_pShader->setVec3("objectColor", m_color[0], m_color[1], m_color[2]);
        m_pShader->setVec3("lightColor", m_lightColor[0], m_lightColor[1], m_lightColor[2]);
        m_pShader->setVec3("lightPos", m_lightPos.x, m_lightPos.y, m_lightPos.z);
        m_pShader->setVec3("viewPos", m_cameraPos.x, m_cameraPos.y, m_cameraPos.z);

        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, m_pos);
        m_pShader->setMatrix("model", glm::value_ptr(model));
        glBindVertexArray(m_VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
    }

    std::shared_ptr<ShaderProgram>& Cube::shader() {
        return m_pShader;
    }
